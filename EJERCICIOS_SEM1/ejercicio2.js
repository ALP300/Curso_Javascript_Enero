//2. Determina el rango de un número:
//Declara una variable número con un valor numérico y utiliza condicionales para
//determinar en qué rango se encuentra.
let numero= 75;
if(numero>=0 && numero<=30){
    console.log("El número está en el rango de 0 a 30");
}else if(numero>=31 && numero<=60){
    console.log("El número está en el rango de 31 a 60");
}else if(numero>=61 && numero<=70){
    console.log("El número está en el rango de 0 a 70");
}else{
    console.log("El número está fuera del rango")
}